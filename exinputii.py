# Exemplo de como utilizar o comando input
#  Peça para o usuário digitar dois números inteiros.
# Exiba ao final a soma destes números.

print("Digite um número inteiro:")
num1 = input()
print("Digite outro número inteiro:")
num2 = input()

print("Você digitou:", num1, "e", num2, "\nVamos fazer a soma entre eles!")

resultado = int(num1) + int(num2)

print(num1,"+",num2,"=", resultado)

